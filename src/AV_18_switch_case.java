import java.util.Scanner;


public class AV_18_switch_case {
    public static void main(String[] args) {
        String var = "luna";

        switch (var) {
            case "lily" -> System.out.println("You are going to become an Adult!");
            case "luna" -> System.out.println("You are going to join a Job!");
            case "suzie" -> System.out.println("You are going to get retired!");
            default -> System.out.println("Enjoy Your life!");
        }
        System.out.println("Thanks for being a magic Cookie!");}}

